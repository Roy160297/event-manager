export default async function EventManagementDexPage() {
  const currentStaff = await getCurrentStaff();
  const canReadDex = !!currentStaff && canRead(currentStaff.permissions, "event_management_dex");

  if (!canReadDex) return <NoPermissionNotice />;

  return (
    <div className="flex flex-col gap-6">
      <h1 className="text-2xl font-bold">ניהול אירוע (Dex)</h1>
      <p className="text-sm text-foreground/60">
        מדריך למנהל האירוע לאורך כל האירוע - מהכנות לפני האירוע ועד סופו.
      </p>

      {CATEGORIES.map((category) => (
        <div key={category.label} className={cardClass}>
          <p className="text-sm font-semibold underline">{category.label}</p>
          <ul className={listClass}>
            {category.items.map((item) => (
              <li key={item}>{item}</li>
            ))}
          </ul>
        </div>
      ))}
    </div>
  );
}
